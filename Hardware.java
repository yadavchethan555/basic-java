class Hardware{

private String tool;
private String name;
private int price;




public Hardware(){
	
	
}

public void setTool(String tool){
	this.tool = tool;
}

public String getTool(){
	return tool;
}

public void setName(String name){
	this.name = name;
}
public String getName(){
	return name;
}

public void setPrice(int price){
	this.price = price;
}

public int getPrice(){
	return price;
}


}