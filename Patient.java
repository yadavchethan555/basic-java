class Patient{

private String name;
private String place;
private int id;
private int price;




public Patient(){
	
}

public void setName(String name){
	this.name = name;
}

public String getName(){
	return name;
}

public void setPlace(String place){
	this.place = place;
	
}


public String  getPlace(){
	
	return place;
}


public void setId(int id){
	this.id =id;
}

public int getId(){
	return id;
}

public void setPrice(int price){
	this.price =price;
}

public int getPrice(){
	return price;
}





}